let theme = "blue";

switch (theme) {
    case "light":
        console.log("light theme applied");
        break;
    case "Dark": {
            console.log("Dark theme applied");
            break;
    }
    case "blue": {
            console.log("blue theme applied");
            break;
    }
    default: 
            console.log("default theme applied");
    
}
